def linearSearch(contacts, target):
    for i,contact in enumerate(contacts):
        if contact['name']==target:
            return i,contact
    return None
#--------------------------------------------------------------------------
class treeNode:
    def __init__(self,key):
        self.key = key
        self.right = None
        self.left = None
    
def insertTree(root, key):
    if root == None:
        return treeNode(key)
    if key > root.key:
        root.right = insertTree(root.right, key)
    else:
        root.left = insertTree(root.left, key)
    return root

def searchTree(root, target):
    if root.key == target:
        return root
    elif target > root.key:
        return searchTree(root.right, target)
    elif target < root.key:
        return searchTree(root.left, target)

